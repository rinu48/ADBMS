SELECT * FROM SAILORS,RESERVES WHERE SAILORS.SID=RESERVES.SID AND BID = 101;
SELECT SNAME FROM SAILORS WHERE RATING > 7;
SELECT SNAME FROM SAILORS,RESERVES,BOATS WHERE BOATS.BID = 103 AND BOATS.BID = RESERVES.BID AND SAILORS.SID = RESERVES.SID;
SELECT DISTINCT SNAME FROM SAILORS,RESERVES,BOATS WHERE COLOR='RED' AND BOATS.BID = RESERVES.BID AND SAILORS.SID = RESERVES.SID ORDER BY SAILORS.AGE;
SELECT DISTINCT SNAME FROM SAILORS,RESERVES,BOATS WHERE (COLOR ='RED' OR COLOR = 'GREEN') AND BOATS.BID = RESERVES.BID AND SAILORS.SID=RESERVES.SID;
SELECT DISTINCT COLOR FROM SAILORS,RESERVES,BOATS WHERE SNAME = 'LUBBER' AND BOATS.BID = RESERVES.BID AND SAILORS.SID=RESERVES.SID;
SELECT DISTINCT SNAME FROM SAILORS,RESERVES,BOATS WHERE COLOR = 'RED' AND BOATS.BID = RESERVES.BID AND SAILORS.SID=RESERVES.SID
UNION
SELECT DISTINCT SNAME FROM SAILORS,RESERVES,BOATS WHERE COLOR = 'GREEN' AND BOATS.BID = RESERVES.BID AND SAILORS.SID=RESERVES.SID;
SELECT DISTINCT SNAME FROM SAILORS,RESERVES WHERE SAILORS.SID=RESERVES.SID;
SELECT SNAME,RESERVES.SID FROM RESERVES,SAILORS WHERE SAILORS.SID=RESERVES.SID GROUP BY DAYE,RESERVES.SID,SNAME HAVING COUNT(DAYE) > 1; 
SELECT SNAME,AGE FROM SAILORS WHERE AGE = (SELECT MIN(AGE) FROM SAILORS);
SELECT SNAME FROM SAILORS WHERE RATING > (SELECT MAX(RATING) FROM SAILORS WHERE SNAME = 'HORATIO');
SELECT SAILORS.SNAME,SID FROM SAILORS WHERE NOT EXISTS ( SELECT BOATS.BID FROM BOATS WHERE NOT EXISTS
( SELECT BID FROM RESERVES WHERE RESERVES.BID = BOATS.BID AND RESERVES.SID = SAILORS.SID ) );
SELECT COUNT(DISTINCT SNAME) AS NUMBER_OF_DIFFERENT_SAILOR_NAME FROM SAILORS;
SELECT AVG(AGE) AS AVERAGE_AGE_OF_ALL_SAILORS FROM SAILORS;
SELECT RATING, AVG(AGE) AS AVERAGE_AGE_BY_RATING FROM SAILORS GROUP BY RATING;
SELECT RATING, AVG(AGE) AS AVERAGE_AGE FROM SAILORS GROUP BY RATING HAVING COUNT(*) > 1;
