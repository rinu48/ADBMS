CREATE TABLE CUSTOMER
(
  CUST_ID INT NOT NULL, CUST_NAME VARCHAR(20) NOT NULL, PRIMARY KEY(CUST_ID)
);
CREATE TABLE ITEM
(
  ITEM_ID INT NOT NULL, ITEM_NAME VARCHAR(25), PRICE INT NOT NULL, PRIMARY KEY(ITEM_ID)
);
CREATE TABLE SALES
(
  BILL_NO INT, BILL_DATE DATE, CUST_ID INT REFERENCES CUSTOMER(CUST_ID),
  ITEM_ID INT REFERENCES ITEM(ITEM_ID), QTY_SOLD INT, PRIMARY KEY(BILL_NO)
);
